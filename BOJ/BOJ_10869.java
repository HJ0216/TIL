import java.util.Scanner;

public class Main {
	public static void main(String[] args) {
		
		Scanner scan = new Scanner(System.in);
		int a1 = scan.nextInt();
		int a2 = scan.nextInt();
		
		System.out.println(a1 + a2);
		System.out.println(a1 - a2);
		System.out.println(a1 * a2);
		System.out.println(a1 / a2);
		System.out.println(a1 % a2);
		
	}
}
