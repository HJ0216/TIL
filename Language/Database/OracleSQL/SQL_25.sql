-- ABS(n) 절대값을 구하는 함수
SELECT ABS(35) FROM DUAL; -- 35
SELECT ABS(-35) FROM DUAL; -- 35


-- SIGN(n) 음수/양수를 알려주는 함수
SELECT SIGN(35) FROM DUAL; -- 1
SELECT SIGN(-35) FROM DUAL; -- -1
SELECT SIGN(0) FROM DUAL;-- 0

-- ROUND(n, i): 숫자의 반올림 값을 알려주는 함수
SELECT ROUND(34.45678) FROM DUAL; -- 34
SELECT ROUND(34.56789) FROM DUAL; -- 35

SELECT ROUND(12.3456, 2) FROM DUAL; -- 12.35
SELECT ROUND(12.3456, 3) FROM DUAL; -- 12.346

-- MOD(n1, n2): 숫자의 나머지 값 반환
SELECT TRUNC(17/5) "몫", MOD(17, 5) "나머지" FROM DUAL; -- 3 2

-- POWER(n1, n2) / SQRT(n): 숫자의 제곱을 구하는 함수와 제곱근을 구하는 함수
SELECT POWER(5, 2) FROM DUAL; -- 25
SELECT SQRT(25) FROM DUAL; -- 5
